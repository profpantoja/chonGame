package chon.group.game.loader.config.environment.ui;

public class PanelConfig {

    private int width;
    private int height;

    public PanelConfig() {}

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }
}