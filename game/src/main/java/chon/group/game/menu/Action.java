package chon.group.game.menu;

public enum Action {

    /* It Enters in a new Menu. */
    ENTER,
    /* It goes back to a previous Menu. */
    POP,
    /* It skips a Story or Screen. */
    SKIP,
    /* It restarts the Game. */
    RESET,
    /* It starts the Game. */
    START,
    /* It goes back to the beginning of the Level. */
    CONTINUE,
    /* It activates the debug mode. */
    DEBUG,
    /* An empty state. */
    NONE

}
